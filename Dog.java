public class Dog{
  private String breed;
  private String colour;
  private String sex;
  private String age;

   Dog(String breed , String colour, String sex , String age){
   this.breed = breed;
   this.colour = colour;
   this.sex = sex;
   this.age = age;
   }
   public String getBreed(){
     return this.breed;
   }
     public String getColour(){
       return this.colour;
     }
       public String getSex(){
         return this.sex;
       }
         public String getAge(){
           return this.age;

         }
       }
